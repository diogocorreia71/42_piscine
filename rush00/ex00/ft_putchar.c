#include <unistd.h>

void	ft_putchar(char a, char b, char c, int k)
{
	int	xb;

	write (1, &a, 1);
	xb = k - 2;
	while (xb > 0)
	{
		write (1, &b, 1);
		xb--;
	}
	if (k >= 2)
	{
		write (1, &c, 1);
	}
}
